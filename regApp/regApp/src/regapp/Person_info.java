    private void btn_attach_imageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_attach_imageActionPerformed
        // TODO add your handling code here:
        
        JFileChooser chooser = new JFileChooser();
        chooser.showOpenDialog(null);
        File file = chooser.getSelectedFile();
        filename = file.getAbsolutePath();
        
        image_field.setText(filename);
        
        ImageIcon icon = new ImageIcon(filename);
        Image img = icon.getImage();
        BufferedImage bi = new BufferedImage(img.getHeight(null),img.getWidth(null),BufferedImage.TYPE_INT_ARGB);
        Graphics g = bi.createGraphics();
        g.drawImage(img, 120, 90, HEIGHT,WIDTH,null);
        ImageIcon newIcon = new ImageIcon(bi);
        personImage.setIcon(icon);
        
        try{
            File image = new File(filename);
            FileInputStream fis = new FileInputStream(image);
            ByteArrayOutputStream bos = new ByteArrayOutputStream();
            byte[] buf = new byte[1024];
            
            for (int read; (read=fis.read(buf))!=-1;){
                bos.write(buf,0,read);
            }
            
            person_image=bos.toByteArray();
            
            }catch(Exception ex){
                ex.printStackTrace();
            }
        
        
        
    }//GEN-LAST:event_btn_attach_imageActionPerformed

